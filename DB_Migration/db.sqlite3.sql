BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "info_teacher" (
	"id"	varchar(100) NOT NULL,
	"name"	varchar(100) NOT NULL,
	"sex"	varchar(50) NOT NULL,
	"DOB"	date NOT NULL,
	"user_id"	integer UNIQUE,
	"dept_id"	varchar(100) NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("user_id") REFERENCES "info_user"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("dept_id") REFERENCES "info_dept"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_assigntime" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"period"	varchar(50) NOT NULL,
	"assign_id"	integer NOT NULL,
	"day"	varchar(15) NOT NULL,
	FOREIGN KEY("assign_id") REFERENCES "info_assign"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_assign" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"class_id_id"	varchar(100) NOT NULL,
	"course_id"	varchar(50) NOT NULL,
	"teacher_id"	varchar(100) NOT NULL,
	FOREIGN KEY("teacher_id") REFERENCES "info_teacher"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("class_id_id") REFERENCES "info_class"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("course_id") REFERENCES "info_course"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_student" (
	"USN"	varchar(100) NOT NULL,
	"name"	varchar(200) NOT NULL,
	"sex"	varchar(50) NOT NULL,
	"class_id_id"	varchar(100) NOT NULL,
	"user_id"	integer UNIQUE,
	"DOB"	date NOT NULL,
	FOREIGN KEY("class_id_id") REFERENCES "info_class"("id") DEFERRABLE INITIALLY DEFERRED,
	PRIMARY KEY("USN"),
	FOREIGN KEY("user_id") REFERENCES "info_user"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "django_session" (
	"session_key"	varchar(40) NOT NULL,
	"session_data"	text NOT NULL,
	"expire_date"	datetime NOT NULL,
	PRIMARY KEY("session_key")
);
CREATE TABLE IF NOT EXISTS "django_admin_log" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"action_time"	datetime NOT NULL,
	"object_id"	text,
	"object_repr"	varchar(200) NOT NULL,
	"change_message"	text NOT NULL,
	"content_type_id"	integer,
	"user_id"	integer NOT NULL,
	"action_flag"	smallint unsigned NOT NULL,
	FOREIGN KEY("content_type_id") REFERENCES "django_content_type"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("user_id") REFERENCES "info_user"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_attendance" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"date"	date NOT NULL,
	"status"	bool NOT NULL,
	"attendanceclass_id"	integer NOT NULL,
	"course_id"	varchar(50) NOT NULL,
	"student_id"	varchar(100) NOT NULL,
	FOREIGN KEY("attendanceclass_id") REFERENCES "info_attendanceclass"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("student_id") REFERENCES "info_student"("USN") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("course_id") REFERENCES "info_course"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_attendancetotal" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"course_id"	varchar(50) NOT NULL,
	"student_id"	varchar(100) NOT NULL,
	FOREIGN KEY("student_id") REFERENCES "info_student"("USN") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("course_id") REFERENCES "info_course"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_class" (
	"id"	varchar(100) NOT NULL,
	"section"	varchar(100) NOT NULL,
	"sem"	integer NOT NULL,
	"dept_id"	varchar(100) NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("dept_id") REFERENCES "info_dept"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_course" (
	"id"	varchar(50) NOT NULL,
	"name"	varchar(50) NOT NULL,
	"shortname"	varchar(50) NOT NULL,
	"dept_id"	varchar(100) NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("dept_id") REFERENCES "info_dept"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_marks" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"name"	varchar(50) NOT NULL,
	"marks1"	integer NOT NULL,
	"studentcourse_id"	integer NOT NULL,
	FOREIGN KEY("studentcourse_id") REFERENCES "info_studentcourse"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_studentcourse" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"course_id"	varchar(50) NOT NULL,
	"student_id"	varchar(100) NOT NULL,
	FOREIGN KEY("student_id") REFERENCES "info_student"("USN") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("course_id") REFERENCES "info_course"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_marksclass" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"name"	varchar(50) NOT NULL,
	"status"	bool NOT NULL,
	"assign_id"	integer NOT NULL,
	FOREIGN KEY("assign_id") REFERENCES "info_assign"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_dept" (
	"id"	varchar(100) NOT NULL,
	"name"	varchar(200) NOT NULL,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "info_attendanceclass" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"date"	date NOT NULL,
	"status"	integer NOT NULL,
	"assign_id"	integer NOT NULL,
	FOREIGN KEY("assign_id") REFERENCES "info_assign"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_user_user_permissions" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"user_id"	integer NOT NULL,
	"permission_id"	integer NOT NULL,
	FOREIGN KEY("permission_id") REFERENCES "auth_permission"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("user_id") REFERENCES "info_user"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_user_groups" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"user_id"	integer NOT NULL,
	"group_id"	integer NOT NULL,
	FOREIGN KEY("group_id") REFERENCES "auth_group"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("user_id") REFERENCES "info_user"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "info_user" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"password"	varchar(128) NOT NULL,
	"last_login"	datetime,
	"is_superuser"	bool NOT NULL,
	"username"	varchar(150) NOT NULL UNIQUE,
	"first_name"	varchar(30) NOT NULL,
	"last_name"	varchar(150) NOT NULL,
	"email"	varchar(254) NOT NULL,
	"is_staff"	bool NOT NULL,
	"is_active"	bool NOT NULL,
	"date_joined"	datetime NOT NULL
);
CREATE TABLE IF NOT EXISTS "auth_permission" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"content_type_id"	integer NOT NULL,
	"codename"	varchar(100) NOT NULL,
	"name"	varchar(255) NOT NULL,
	FOREIGN KEY("content_type_id") REFERENCES "django_content_type"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "auth_group_permissions" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"group_id"	integer NOT NULL,
	"permission_id"	integer NOT NULL,
	FOREIGN KEY("permission_id") REFERENCES "auth_permission"("id") DEFERRABLE INITIALLY DEFERRED,
	FOREIGN KEY("group_id") REFERENCES "auth_group"("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE TABLE IF NOT EXISTS "auth_group" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"name"	varchar(80) NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS "django_content_type" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"app_label"	varchar(100) NOT NULL,
	"model"	varchar(100) NOT NULL
);
CREATE TABLE IF NOT EXISTS "django_migrations" (
	"id"	integer NOT NULL PRIMARY KEY AUTOINCREMENT,
	"app"	varchar(255) NOT NULL,
	"name"	varchar(255) NOT NULL,
	"applied"	datetime NOT NULL
);
